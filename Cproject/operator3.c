#include <stdio.h>

int main8() {

	//산술연산자
	//+,-,*,/...

	// + :  왼쪽값과 오른쪽값을 더하기
	// - :  왼쪽값에서 오른쪽값을 빼기
	//

	int num1 = 7, num2 = 3;
	printf("%d + %d = %d\n", num1, num2, num1 + num2);
	printf("%d - %d = %d\n", num1, num2, num1 - num2);
	printf("%d * %d = %d\n", num1, num2, num1 * num2);
	printf("%d / %d = %d\n", num1, num2, num1 / num2);
	printf("%d %% %d = %d\n", num1, num2, num1 % num2);



	return 0;
}